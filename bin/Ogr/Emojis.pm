sub emoji_rand
{
	if (scalar @unicodes != scalar @descriptions)
	{
		die "Emoji arrays are not aligned\n";
	}

	my $i = int rand(scalar @unicodes);

	return ($unicodes[$i], $descriptions[$i]);
}

